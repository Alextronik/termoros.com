<p>Для проектных, монтажных и торговых организаций компания
&laquo;Терморос&raquo; регулярно проводит семинары по эксплуатации, проектированию
и монтажу оборудования.</p>

<p><span style="font-size: 120%; color: rgb(217, 33, 39);">Семинары в главном офисе</span><br></p>

<p>Компания &laquo;Терморос&raquo; регулярно проводит обучающие
семинары по эксплуатации, проектированию и монтажу оборудования для
проектных, монтажных и торговых организаций.
Особое внимание на семинарах уделяется не только теоретическим и
практическим основам, но и особенностям подбора и настройки
оборудования.<br>
В испытательной лаборатории ГК &laquo;Терморос&raquo;
производится настройка и запуск оборудования, а также моделируются
форс-мажорные ситуации и способы их устранения.
График проведения семинаров обновляется 2 раза в год.<br>
Приглашаем всех желающих принять участие в данных мероприятиях.<br>
Участие в семинарах бесплатное.<br>
Регистрация обязательна.</p>

<p><span style="font-size: 120%; color: rgb(217, 33, 39);">Обучение в регионах</span></p>

<p>Мы также проводим обучение в регионах. Информация о планируемых семинарах будет публиковаться на сайте.</p>

<p><span style="font-size: 120%; color: rgb(217, 33, 39);">Выезды на производство</span></p>

<p>​Несколько раз в год &laquo;Терморос&raquo; организуют поездки на заводы производителей отопительного оборудования: в Италию, Бельгию, Германию.<br>
В зарубежных поездках приняли участие уже более 400 представителей
торговых и монтажных компаний - партнеров из разных уголков России.
Это важный опыт, который дает специалистам возможность напрямую
ознакомиться с производственным процессом, увидеть лабораторные
испытания продукции на прочность, обсудить вопросы внедрения и
эксплуатации оборудования в существующих объектах.<br>
Одним из ключевых моментов коллективных туров на заводы является
возможность личного общения с инженерами и сотрудниками предприятия,
которые предоставляют максимально полную информацию о продукции и
производстве.
</p>
